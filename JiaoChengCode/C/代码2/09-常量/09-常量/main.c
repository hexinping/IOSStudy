#include <stdio.h>

int main(int argc, const char * argv[])
{

    10;//整形常量
    9.9;//小数默认就是double类型的
    9.2f;//float类型
    'a';//字符型
    '1';
//    '南'; 汉子占3个字节
    "Hello World";//字符串常量
    printf("Hello World");
    return 0;
}

